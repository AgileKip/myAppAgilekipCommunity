    @Test
    @Transactional
    void createOrder() throws Exception {
        int databaseSizeBeforeCreate = orderRepository.findAll().size();
        // Create the Order
        OrderDTO orderDTO = orderMapper.toDto(order);
        restOrderMockMvc
            .perform(post(ENTITY_API_URL).contentType(MediaType.APPLICATION_JSON).content(TestUtil.convertObjectToJsonBytes(orderDTO)))
            .andExpect(status().isCreated());

        // Validate the Order in the database
        List<Order> orderList = orderRepository.findAll();
        assertThat(orderList).hasSize(databaseSizeBeforeCreate + 1);
        Order testOrder = orderList.get(orderList.size() - 1);
        assertThat(testOrder.getNumber()).isEqualTo(DEFAULT_NUMBER);
        assertThat(testOrder.getStreetAddress()).isEqualTo(DEFAULT_STREET_ADDRESS);
        assertThat(testOrder.getPostalCode()).isEqualTo(DEFAULT_POSTAL_CODE);
        assertThat(testOrder.getCity()).isEqualTo(DEFAULT_CITY);
        assertThat(testOrder.getStateProvince()).isEqualTo(DEFAULT_STATE_PROVINCE);
        assertThat(testOrder.getDeliveryMethod()).isEqualTo(DEFAULT_DELIVERY_METHOD);
        assertThat(testOrder.getCcNumber()).isEqualTo(DEFAULT_CC_NUMBER);
        assertThat(testOrder.getCcDate()).isEqualTo(DEFAULT_CC_DATE);
        assertThat(testOrder.getCcVerifierDigit()).isEqualTo(DEFAULT_CC_VERIFIER_DIGIT);
        assertThat(testOrder.getTotal()).isEqualTo(DEFAULT_TOTAL);
    }

