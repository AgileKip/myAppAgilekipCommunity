    @Test
    @Transactional
    void putNewOrder() throws Exception {
        // Initialize the database
        orderRepository.saveAndFlush(order);

        int databaseSizeBeforeUpdate = orderRepository.findAll().size();

        // Update the order
        Order updatedOrder = orderRepository.findById(order.getId()).get();
        // Disconnect from session so that the updates on updatedOrder are not directly saved in db
        em.detach(updatedOrder);
        updatedOrder
            .number(UPDATED_NUMBER)
            .streetAddress(UPDATED_STREET_ADDRESS)
            .postalCode(UPDATED_POSTAL_CODE)
            .city(UPDATED_CITY)
            .stateProvince(UPDATED_STATE_PROVINCE)
            .deliveryMethod(UPDATED_DELIVERY_METHOD)
            .ccNumber(UPDATED_CC_NUMBER)
            .ccDate(UPDATED_CC_DATE)
            .ccVerifierDigit(UPDATED_CC_VERIFIER_DIGIT)
            .total(UPDATED_TOTAL);
        OrderDTO orderDTO = orderMapper.toDto(updatedOrder);

        restOrderMockMvc
            .perform(
                put(ENTITY_API_URL_ID, orderDTO.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(orderDTO))
            )
            .andExpect(status().isOk());

        // Validate the Order in the database
        List<Order> orderList = orderRepository.findAll();
        assertThat(orderList).hasSize(databaseSizeBeforeUpdate);
        Order testOrder = orderList.get(orderList.size() - 1);
        assertThat(testOrder.getNumber()).isEqualTo(UPDATED_NUMBER);
        assertThat(testOrder.getStreetAddress()).isEqualTo(UPDATED_STREET_ADDRESS);
        assertThat(testOrder.getPostalCode()).isEqualTo(UPDATED_POSTAL_CODE);
        assertThat(testOrder.getCity()).isEqualTo(UPDATED_CITY);
        assertThat(testOrder.getStateProvince()).isEqualTo(UPDATED_STATE_PROVINCE);
        assertThat(testOrder.getDeliveryMethod()).isEqualTo(UPDATED_DELIVERY_METHOD);
        assertThat(testOrder.getCcNumber()).isEqualTo(UPDATED_CC_NUMBER);
        assertThat(testOrder.getCcDate()).isEqualTo(UPDATED_CC_DATE);
        assertThat(testOrder.getCcVerifierDigit()).isEqualTo(UPDATED_CC_VERIFIER_DIGIT);
        assertThat(testOrder.getTotal()).isEqualTo(UPDATED_TOTAL);
    }

